package com.herick;

public class Comodo {

    private int tipo;
    private float largura;
    private float altura;
    private float areaComodo;

    public int getTipo() {
        return tipo;
    }

    public void setTipo(int tipo) {
        this.tipo = tipo;
    }

    public float getLargura() {
        return largura;
    }

    public void setLargura(float largura) {
        this.largura = largura;
    }

    public float getAltura() {
        return altura;
    }

    public void setAltura(float altura) {
        this.altura = altura;
    }

    public float getAreaComodo() {
        return areaComodo;
    }

    public void setAreaComodo(float areaComodo) {
        this.areaComodo = areaComodo;
    }
}
